    private void btAdicionarPecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarPecaActionPerformed
        String nome = tfNomePeca.getText();
        int quantidade = Integer.parseInt(tfQuantidadePeca.getText());
        Double precoUnitario = Double.parseDouble(tfPrecoUnitarioPeca.getText());
        
        if (!nome.isEmpty() && !tfQuantidadePeca.getText().isEmpty() && !tfPrecoUnitarioPeca.getText().isEmpty()) {
            if (linhaSelecionadaPecas >= 0) {
                modeloPecas.removeRow(linhaSelecionadaPecas);
                modeloPecas.insertRow(linhaSelecionadaPecas, new Object[]{nome, String.valueOf(quantidade), precoUnitario.toString()});
                custoPecas -= custoPecaEditado;
                custoTotal -= custoPecaEditado;
                JOptionPane.showMessageDialog(this, "Peça editada com sucesso!");
            } else {
                modeloPecas.addRow(new Object[]{nome, String.valueOf(quantidade), precoUnitario.toString()});
                JOptionPane.showMessageDialog(this, "Peça adicionada com sucesso!");
            }

            try {
                Double preco = precoUnitario * quantidade;
                custoPecas += preco;
                custoTotal += preco;
                lbTotalPecas.setText(custoPecas.toString());
                lbTotal.setText(custoTotal.toString());
            } catch(Exception ex) {
                JOptionPane.showMessageDialog(this, "Informe um preço válido!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            
            tfNomePeca.setText("");
            tfQuantidadePeca.setText("");
            tfPrecoUnitarioPeca.setText("");
            tfNomePeca.requestFocus();
            linhaSelecionadaPecas = -1;
        } else {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos!");
        }
    }//GEN-LAST:event_btAdicionarPecaActionPerformed

    private void btEditarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarServicoActionPerformed
        linhaSelecionadaServicos = -1;
        linhaSelecionadaServicos = tbServicos.getSelectedRow();
        
        if(linhaSelecionadaServicos >= 0) {
            String descricao = (String)tbServicos.getValueAt(linhaSelecionadaServicos, 0);
            String custo = (String)tbServicos.getValueAt(linhaSelecionadaServicos, 1);
            
            custoServicoEditado = Double.parseDouble(custo);
            
            tfDescricaoServico.setText(descricao);
            tfCustoServico.setText(custo);
        } else {
                JOptionPane.showMessageDialog(this, "Selecione um registro na tabela!");           
        }
    }//GEN-LAST:event_btEditarServicoActionPerformed

    private void btEditarPecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarPecaActionPerformed
        linhaSelecionadaPecas = -1;
        linhaSelecionadaPecas = tbPecas.getSelectedRow();
        
        if(linhaSelecionadaPecas >= 0) {
            String nome = (String)tbPecas.getValueAt(linhaSelecionadaPecas, 0);
            String quantidade = (String)tbPecas.getValueAt(linhaSelecionadaPecas, 1);
            String preco = (String)tbPecas.getValueAt(linhaSelecionadaPecas, 2);
            
            custoPecaEditado = Double.parseDouble(preco) * Integer.parseInt(quantidade);

            tfNomePeca.setText(nome);
            tfQuantidadePeca.setText(quantidade);
            tfPrecoUnitarioPeca.setText(preco);
        } else {
                JOptionPane.showMessageDialog(this, "Selecione um registro na tabela!");           
        }
    }//GEN-LAST:event_btEditarPecaActionPerformed

