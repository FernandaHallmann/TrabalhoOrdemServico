    private void btAdicionarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarServicoActionPerformed
        String descricao = tfDescricaoServico.getText();
        Double custo = Double.parseDouble(tfCustoServico.getText());
        
        if (!descricao.isEmpty() && !tfCustoServico.getText().isEmpty()) {
            if (linhaSelecionadaServicos >= 0) {
                modeloServico.removeRow(linhaSelecionadaServicos);
                modeloServico.insertRow(linhaSelecionadaServicos, new Object[]{descricao, custo.toString()});
                custoServicos -= custoServicoEditado;
                custoTotal -= custoServicoEditado;
                JOptionPane.showMessageDialog(this, "Serviço editado com sucesso!");
            } else {
                modeloServico.addRow(new Object[]{descricao, custo.toString()});
                JOptionPane.showMessageDialog(this, "Serviço adicionado com sucesso!");
            }

            try {
                custoServicos += custo;
                custoTotal += custo;
                lbPrecoTotalServicos.setText(custoServicos.toString());
                lbTotal.setText(custoTotal.toString());
            } catch(Exception ex) {
                JOptionPane.showMessageDialog(this, "Informe um custo válido!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            
            tfDescricaoServico.setText("");
            tfCustoServico.setText("");
            tfDescricaoServico.requestFocus();
            linhaSelecionadaServicos = -1;
        } else {
            JOptionPane.showMessageDialog(this, "Os campos de descrição e custo devem ser preenchidos!");
        }
    }//GEN-LAST:event_btAdicionarServicoActionPerformed

